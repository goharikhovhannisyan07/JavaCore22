public class ArrayExample {
    public static void main(String[] args) {
        int[] numbers = new int[8];
        numbers[0] = 85;
        numbers[3] = 53;
        for (int i = 0; i < numbers.length; i++) {
            System.out.print(numbers[i] + " ");

        }
    }
}
