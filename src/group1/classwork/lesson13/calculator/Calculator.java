package group1.classwork.lesson13.calculator;

public class Calculator {

    double plus(double a, double b) {
        double g = a + b;
        return g;
    }

    double minus(double c, double d) {
        double g = c - d;
        return g;
    }

    double multiple(double x, double y) {
        double g = x * y;
        return g;
    }

    double divide(double k, double j) {
        double g = k / j;
        return g;
    }


}
